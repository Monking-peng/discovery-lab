export const messages: Record<Locale, Record<MessageKey, string>> = {
  en: enMessages,
  "zh-CN": zhCNMessages,
};

export function isLocale(value: unknown): value is Locale {
  return typeof value === "string" && SUPPORTED_LOCALES.includes(value as Locale);
}

export function normalizeLocale(value: unknown): Locale | null {
  if (typeof value !== "string") return null;
  const normalized = value.trim().replaceAll("_", "-").toLowerCase();
  if (normalized === "en" || normalized.startsWith("en-")) return "en";
  if (normalized === "zh" || normalized === "zh-cn" || normalized === "zh-sg" || normalized === "zh-hans" || normalized.startsWith("zh-hans-")) return "zh-CN";
  return null;
}

export function getInitialLocale(...candidates: readonly unknown[]): Locale {
  for (const candidate of candidates) {
    const locale = normalizeLocale(candidate);
    if (locale) return locale;
  }
  return DEFAULT_LOCALE;
}

export function translate(locale: Locale, key: MessageKey, vars: TranslationVars = {}): string {
  return messages[locale][key].replace(/\{([a-zA-Z0-9_]+)\}/g, (token, name: string) => (
    Object.prototype.hasOwnProperty.call(vars, name) ? String(vars[name]) : token
  ));
}

export function isMessageKey(value: string): value is MessageKey {
  return Object.prototype.hasOwnProperty.call(enMessages, value);
}

export function parseLocaleCookie(cookieHeader: string): Locale | null {
  const value = cookieHeader.split(";").map((part) => part.trim()).find((part) => part.startsWith(`${LOCALE_COOKIE_NAME}=`));
  return value ? normalizeLocale(decodeURIComponent(value.slice(LOCALE_COOKIE_NAME.length + 1))) : null;
}

export function serializeLocaleForStorage(locale: Locale): string {
  return locale;
}

export function serializeLocaleCookie(locale: Locale): string {
  return `${LOCALE_COOKIE_NAME}=${encodeURIComponent(locale)}; Path=/; Max-Age=31536000; SameSite=Lax`;
}
